export function iconSvgUrl(icon: IconEntry): string {
  // icon.s is the full path suffix under assets/<icon.d>/, e.g.:
  //   "Flat/grinning_face_flat" or "Default/Flat/person_superhero_flat_default"
  return `https://cdn.jsdelivr.net/gh/microsoft/fluentui-emoji@main/assets/${encodeURIComponent(icon.d)}/${icon.s}.svg`;
}

export function iconByEmoji(emoji: string): IconEntry | undefined {
  return ICONS.find(i => i.e === emoji);
}
